<h3>Jelen vagyunk!</h3>
<p>Több éve veszünk részt a Csillagászat napja alkalmából világszerte megrendezésre kerülő Járdacsillagászaton, melynek keretén belül megmutatjuk távcsövön keresztül a járókelőknek az éppen aktuális látnivalókat (Pl Hold, Jupiter, Szaturnusz és egyebek). A rendezvény után is, ha lehetőségünk és az időjárás engedi kivonulunk távcsöveinkkel az Aba-Novák Kulturális Központ elé azon a szombat estén ami a legközelebb esik a Hold első negyed állásához. Így áprilistól egészen októberig érdemes minket a Hild téren keresni, azon szombat estéken amikor a Hold első negyed állapotában van.</p>
<p>Évek óta részt veszünk a "Múzeumok éjszakáján" is (minden év júniusának végén), ilyenkor távcsövekkel, számítógéppel és projektorral tartunk tudományos előadásokat. 2018-ban is a Toronyházban várjuk kedves látogatóinkat június 23-án szombat délután 18:30 kezdéssel, ugyanúgy, mint az ezt megelőzőerendezvényünkön.</p></br></br>
<h3>Minden év augusztus közepén Meteorhullás megfigyelést szoktunk tartani a Szandaszőlős melletti Sport reptéren.
</h3>
<p>
2014-ben szervezetünk először csatlakozott a "Kutatók Éjszakája" rendezvénysorozathoz, azóta minden évben megrendezzük és csatlakozunk ehhez a kezdeményezéshez is. Ezen alkalomból hosszabb nyitva tartással vártuk kedves vendégeinket és látogatóinkat szeptember utolsó pénteki és szombati napján,  számos érdekes és látványos előadással. Az előadások után hangulatos esti távcsövezésre invitáljuk látogatóinkat.
</p>
<h3>Mi is a TIT?</h3>
<p>
A TIT Városi Szervezetének Kopernikusz Köre videovetítéssel egybekötött elõadásokkal és távcsöves bemutatókkal várja kedves látogatóit minden pénteken 19 órától a Jubileum téri toronyház XXIII. emeletén, a Kopernikusz teremben.Egy 20 cm-es Newton távcsõvel, egy 12 cm-es Makszutov-Cassegrain távcsõvel, egy 15 cm-es lencsés távcsővel és egy 6-cm es Telementorral az éjszakai égbolt számos látnivalóját megcsodálhatják a látogatók. Megismerkedhetnek, a bolygókkal, a Holddal és a Nappal. Az előadóteremben korszerû audio-vizuális technikával, 5.1-es hangrendszerben a legújabb tudományos filmek kerülnek bemutatásra.
</p>
